const text = "NyffsGeyylB";

function accum(s) {
    const textArr = s.toLowerCase().split("");

    let result = textArr.map((el, idx) => {
        let str = "";
        for (let i = 0; i <= idx; i++) str += el;
        return str.charAt(0).toUpperCase() + str.substr(1)
    })
    return result.join("-");
}

